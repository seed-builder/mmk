<?php

namespace App\Providers;

use App\Models\Busi\Customer;
use App\Models\Busi\Employee;
use App\Models\Busi\Store;
use App\Models\Busi\WfChangeList;
use App\Models\Busi\WorkFlowTask;
use App\Repositories\EmployeeRepo;
use App\Repositories\ISysConfigRepo;
use App\Repositories\SysConfigRepo;
use App\Services\DataSync\CustomerFilter;
use App\Services\DataSync\DataSyncSvr;
use App\Services\DataSync\DefaultFilter;
use App\Services\DataSync\DisplayPolicyStoreFilter;
use App\Services\DataSync\EmployeeFilter;
use App\Services\DataSync\KingdeeWorker;
use App\Services\DataSync\StockInFilter;
use App\Services\Sms\AliDaYuSms;
use App\Services\Sms\ISmsSvr;
use App\Services\WorkFlow\Engine;
use Illuminate\Support\Facades\Request;
use Illuminate\Support\ServiceProvider;
use Illuminate\Database\Eloquent\Relations\Relation;

class AppServiceProvider extends ServiceProvider
{
	//protected $defer = true;
    /**
     * Bootstrap any application services.
     *
     * @return void
     */
    public function boot()
    {
        //
	    $this->loadConfig();
	    Relation::morphMap([
		    'employee' => Employee::class,
		    'customer' => Customer::class,
		    'wf_change_list' => WfChangeList::class,
		    'store' => Store::class,
		    //'store' => WorkFlowTask::class,
	    ]);
	    //Engine::boot();
    }

    protected function loadConfig(){
	    $request = Request::instance();
	    $path = $request->path();
	    //LogSvr::routeSvr()->info($path);
	    if(preg_match('/^api/', $path)) {
	    }
	    if(preg_match('/^\//', $path)) {
	    }
	    if(preg_match('/^admin/', $path)) {
		    config([
			    'auth.model' => User::class,
		    ]);
	    }
	    if(preg_match('/^customer/', $path)) {
		    config([
			    'auth.defaults.guard' => 'customer',
			    'auth.model' => Customer::class,
		    ]);
	    }

    }
    /**
     * Register any application services.
     *
     * @return void
     */
    public function register()
    {
        //
	    $this->app->singleton('dataSync', function($app){
	    	$svr = new DataSyncSvr();
	    	$svr->registerFilter(DefaultFilter::class);
	    	$svr->registerFilter(DisplayPolicyStoreFilter::class, 'exp_display_policy_store');
	    	$svr->registerFilter(EmployeeFilter::class, 'bd_employees');
	    	$svr->registerFilter(CustomerFilter::class, 'bd_customers');
	    	$svr->registerFilter(StockInFilter::class, 'st_stock_ins');
	    	$svr->registerWorker(KingdeeWorker::class);
	    	return $svr;
	    });
	    $this->app->singleton(ISysConfigRepo::class, SysConfigRepo::class);
	    $this->app->singleton(ISmsSvr::class, AliDaYuSms::class);
	    $this->app->singleton(ISmsSvr::class, AliDaYuSms::class);
	    $this->app->bind('employee-repo', EmployeeRepo::class);
	    $this->app->bind('workflowengine', function($app){
	    	return new Engine();
	    });
    }

    public function provides()
    {
	    return [
	    	'dataSync',
		    ISysConfigRepo::class,
		    ISmsSvr::class,
		    'employee-repo',
	    ]; // TODO: Change the autogenerated stub
    }
}
